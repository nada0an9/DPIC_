        public override string[] GetRolesForUser(string username)
        {
            using (DB_Model db = new DB_Model())
            {
                var id = Convert.ToDecimal(username);
                var userRoles = (from user in db.USERS
                                 join userRole in db.USER_ROLE
                                 on user.USER_ID equals userRole.USER_ID
                                 join role in db.ROLES
                                 on userRole.ROLE_ID equals role.ROLE_ID
                                 where user.USER_ID == id
                                 select role.ROLE_NAME).ToArray();
                return userRoles;
            }
        }

